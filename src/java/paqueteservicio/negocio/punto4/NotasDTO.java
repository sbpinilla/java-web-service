package paqueteservicio.negocio.punto4;

/**
 *
 * @author sbpinilla
 */
public class NotasDTO {

    private int numA;
    private int numB;
    private int numC;
    private int numD;
    private int numF;

    public NotasDTO(int numA, int numB, int numC, int numD, int numF) {
        this.numA = numA;
        this.numB = numB;
        this.numC = numC;
        this.numD = numD;
        this.numF = numF;
    }

    
    
    
    public NotasDTO() {
    }

    public int getNumA() {
        return numA;
    }

    public void setNumA(int numA) {
        this.numA = numA;
    }

    public int getNumB() {
        return numB;
    }

    public void setNumB(int numB) {
        this.numB = numB;
    }

    public int getNumC() {
        return numC;
    }

    public void setNumC(int numC) {
        this.numC = numC;
    }

    public int getNumD() {
        return numD;
    }

    public void setNumD(int numD) {
        this.numD = numD;
    }

    public int getNumF() {
        return numF;
    }

    public void setNumF(int numF) {
        this.numF = numF;
    }
    
    
    
    
    

}
